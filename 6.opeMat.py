x = 5 
y = 10
z = 4

print(f"suma de {x} y {y}:", x + y)
print(f"resta de {x} y {y}:", x - y)
print(f"multiplicación de {x} y {y}:", x * y)
print(f"división de {y} y {x}:", y / x)
print(f"división de {y} y {z}:", y / z)
print(f"division redondeada de {y} y {z}:", y//z)
print(f"potencia de {x} al cuadrado", x**2)
print(f"{x} > {z}", x > z)
print(f"{x} < {z}", x < z)
print(f"{x} == {z}", x == z)
print(f"{x} != {z}", x != z)